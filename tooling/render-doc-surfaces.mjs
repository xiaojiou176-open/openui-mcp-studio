#!/usr/bin/env node

import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

import { discoverRegisteredTools } from "./check-doc-completeness.mjs";
import { buildDefaultStages } from "./ci-gate/stages.mjs";

const ROOT = process.cwd();
const REGISTRY_PATH = path.resolve(ROOT, "tooling/contracts/docs-registry.json");
const MANIFEST_PATH = path.resolve(
	ROOT,
	"tooling/contracts/docs-render.manifest.json",
);
const DOCS_COMPLETENESS_PATH = path.resolve(
	ROOT,
	"tooling/contracts/docs-completeness.contract.json",
);
const REMOTE_GOVERNANCE_PATH = path.resolve(
	ROOT,
	"tooling/contracts/remote-governance-evidence.contract.json",
);

async function readJson(filePath) {
	return JSON.parse(await readFile(filePath, "utf8"));
}

function toMarkdownTable(headers, rows) {
	const headerLine = `| ${headers.join(" | ")} |`;
	const dividerLine = `| ${headers.map(() => "---").join(" | ")} |`;
	const rowLines = rows.map((row) => `| ${row.join(" | ")} |`);
	return [headerLine, dividerLine, ...rowLines].join("\n");
}

function stageSummaryMarkdown() {
	const stages = buildDefaultStages({ enforceExternalReadonly: false });
	const formatTaskSemantics = (stage) =>
		stage.tasks
			.map((task) => {
				const mode = task.advisory === true ? "advisory" : "blocking";
				return `\`${task.id}\` (${mode})`;
			})
			.join(", ");
	const rows = stages.map((stage) => [
		`\`${stage.id}\``,
		stage.name,
		`\`${stage.mode}\``,
		formatTaskSemantics(stage),
	]);
return `# CI Stage Reference

This page is generated by \`tooling/render-doc-surfaces.mjs\` from \`tooling/ci-gate/stages.mjs\`. Do not edit it by hand.

## Current Blocking/Advisory Layout

${toMarkdownTable(["Stage", "Name", "Mode", "Tasks"], rows)}

## Notes

- canonical \`ci:gate\` no longer blocks the external-readonly path by default
- external-readonly remains a nightly / report-only path and is not part of the PR blocking path
- the authoritative stage behavior truth comes from \`tooling/ci-gate/stages.mjs\` and \`tooling/ci-gate/core.mjs\`
`;
}

async function workflowInventoryMarkdown() {
	const workflowsDir = path.resolve(ROOT, ".github/workflows");
	const entries = await readdir(workflowsDir, { withFileTypes: true });
	const workflowFiles = entries
		.filter((entry) => entry.isFile() && /\.ya?ml$/u.test(entry.name))
		.map((entry) => entry.name)
		.sort();

	const rows = [];
	for (const fileName of workflowFiles) {
		const absolutePath = path.join(workflowsDir, fileName);
		const content = await readFile(absolutePath, "utf8");
		const nameMatch = content.match(/^name:\s*(.+)$/mu);
		const triggers = [
			["push", /^\s{2}push:\s*$/mu],
			["pull_request", /^\s{2}pull_request:\s*$/mu],
			["schedule", /^\s{2}s?chedule:\s*$/mu],
			["workflow_dispatch", /^\s{2}workflow_dispatch:\s*$/mu],
			["workflow_call", /^\s{2}workflow_call:\s*$/mu],
			]
				.filter(([, pattern]) => pattern.test(content))
				.map(([label]) => label)
				.join(", ");
		rows.push([
			`\`${fileName}\``,
			String(nameMatch?.[1] ?? "").trim(),
			triggers || "-",
		]);
	}

return `# Workflow Inventory

This page is generated by \`tooling/render-doc-surfaces.mjs\` from \`.github/workflows/*\`. Do not edit it by hand.

${toMarkdownTable(["Workflow", "Display Name", "Triggers"], rows)}
`;
}

async function toolMatrixMarkdown(manifest) {
	const docsContract = await readJson(DOCS_COMPLETENESS_PATH);
	const toolNames = await discoverRegisteredTools(ROOT);
	const tools = docsContract.tools ?? {};
	const groupEntries = Object.entries(manifest.toolGroups ?? {});
	const renderedSections = [];

	for (const [groupName, groupTools] of groupEntries) {
		const rows = groupTools.map((toolName) => {
			const entry = tools[toolName] ?? {};
			return [
				`\`${toolName}\``,
				String(entry.minimumRequest ?? "").trim() || "-",
				String(entry.success ?? "").trim() || "-",
				String(entry.failure ?? "").trim() || "-",
			];
		});
		renderedSections.push(
			`## ${groupName}\n\n${toMarkdownTable(
				["Tool", "Minimum Request", "Success", "Failure"],
				rows,
			)}`,
		);
	}

	const ungrouped = toolNames.filter(
		(toolName) =>
			!groupEntries.some(([, groupTools]) =>
				Array.isArray(groupTools) ? groupTools.includes(toolName) : false,
			),
	);
	if (ungrouped.length > 0) {
		const rows = ungrouped.map((toolName) => [toolName]);
		renderedSections.push(
			`## ungrouped\n\n${toMarkdownTable(["Tool"], rows.map((row) => [`\`${row[0]}\``]))}`,
		);
	}

return `# Tool Reference Matrix

This page is generated by \`tooling/render-doc-surfaces.mjs\` from \`tooling/contracts/docs-completeness.contract.json\` and \`services/mcp-server/src/tools/**\`. Do not edit it by hand.

${renderedSections.join("\n\n")}
`;
}

async function docsCatalogMarkdown(registry) {
	const rows = (registry.documents ?? []).map((doc) => [
		`\`${doc.path}\``,
		`\`${doc.role}\``,
	]);
return `# Docs Catalog

This page is generated by \`tooling/render-doc-surfaces.mjs\` from \`tooling/contracts/docs-registry.json\`. Do not edit it by hand.

${toMarkdownTable(["Path", "Role"], rows)}
`;
}

async function remoteGovernanceMarkdown() {
	const contract = await readJson(REMOTE_GOVERNANCE_PATH);
	const renderEvidence = (entries) =>
		entries
			.map((entry) => {
				if (entry.type === "repo_path") {
					return `\`${entry.value}\``;
				}
				return entry.value;
			})
			.join("; ");
	const renderRows = (controls) =>
		controls.map((control) => [
			`\`${control.id}\``,
			control.label,
			`\`${control.status}\``,
			String(control.value ?? "-"),
			renderEvidence(control.evidence ?? []),
		]);

return `# Remote Governance Status

This page is generated by \`tooling/render-doc-surfaces.mjs\` from \`tooling/contracts/remote-governance-evidence.contract.json\`. Do not edit it by hand.

## Boundary

- Repository: \`${contract.repository?.owner ?? ""}/${contract.repository?.name ?? ""}\`
- Checked at: \`${contract.checkedAt ?? ""}\`
- This page distinguishes repository-local verified controls from remote platform controls that may still be unverified in the current execution environment.

## Repository-Local Controls

${toMarkdownTable(["ID", "Label", "Status", "Value", "Evidence"], renderRows(contract.repoLocalControls ?? []))}

## Remote Platform Controls

${toMarkdownTable(["ID", "Label", "Status", "Value", "Evidence"], renderRows(contract.remotePlatformControls ?? []))}
`;
}

async function renderSurfaces() {
	await readJson(REGISTRY_PATH);
	await readJson(MANIFEST_PATH);
	return new Map();
}

async function writeOutputs(outputs) {
	for (const [relativeOutputPath, content] of outputs) {
		const absoluteOutputPath = path.resolve(ROOT, relativeOutputPath);
		await mkdir(path.dirname(absoluteOutputPath), { recursive: true });
		await writeFile(absoluteOutputPath, content);
	}
}

async function checkOutputs(outputs) {
	const mismatches = [];
	for (const [relativeOutputPath, expectedContent] of outputs) {
		const absoluteOutputPath = path.resolve(ROOT, relativeOutputPath);
		let actualContent;
		try {
			actualContent = await readFile(absoluteOutputPath, "utf8");
		} catch {
			mismatches.push(`${relativeOutputPath} is missing. Run npm run docs:render.`);
			continue;
		}
		if (actualContent !== expectedContent) {
			mismatches.push(
				`${relativeOutputPath} is stale. Run npm run docs:render to refresh generated docs.`,
			);
		}
	}
	return mismatches;
}

async function main() {
	const mode = process.argv.includes("--write")
		? "write"
		: process.argv.includes("--check")
			? "check"
			: "check";
	const outputs = await renderSurfaces();

		if (mode === "write") {
			await writeOutputs(outputs);
			console.log(
				`[docs-render] OK (${outputs.size} generated docs required in the minimal docs profile)`,
			);
			return;
		}

	const mismatches = await checkOutputs(outputs);
	if (mismatches.length > 0) {
		console.error("[docs-render] FAILED");
		for (const mismatch of mismatches) {
			console.error(`- ${mismatch}`);
		}
		process.exit(1);
	}

	console.log(`[docs-render] OK (${outputs.size} generated docs are required in the minimal docs profile)`);
}

main().catch((error) => {
	console.error(
		`[docs-render] ERROR: ${error instanceof Error ? error.message : String(error)}`,
	);
	process.exit(1);
});

export {
	stageSummaryMarkdown,
	workflowInventoryMarkdown,
	toolMatrixMarkdown,
	docsCatalogMarkdown,
	remoteGovernanceMarkdown,
	renderSurfaces,
	writeOutputs,
	checkOutputs,
};
